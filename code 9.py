score = int(input('성적을 입력하세요 :'))

if score >= 90:  #성적이 90점 이상인 경우
 print("당신의 학점은 'A'입니다")
elif score >= 80:  #성적이 80점 이상인 경우
 print("당신의 학점은 'B'입니다")
elif score >= 70:  #성적이 70점 이상인 경우
 print("당신의 학점은 'C'입니다")
elif score >= 60:  #성적이 60점 이상인 경우
 print("당신의 학점은 'D'입니다")
else:
 print("당신의 학점은 'F'입니다")        
          